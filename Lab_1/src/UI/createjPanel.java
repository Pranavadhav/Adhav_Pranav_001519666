    private void txtEmailIdKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtEmailIdKeyReleased
        // TODO add your handling code here:
        String emailRegex = "^[A-Z0-9._%+-]+@[A-Z0-9.-]+\\.[A-Z]{2,6}$";
        Pattern emailPat =Pattern.compile(emailRegex,Pattern.CASE_INSENSITIVE);
        Matcher match = emailPat.matcher(txtEmailId.getText());
        if(!match.matches()){
         nlab.setText("incorrect email");
         checkValidity = false;
        }else{
          nlab.setText(null);
          checkValidity = true;
        }
    }//GEN-LAST:event_txtEmailIdKeyReleased
